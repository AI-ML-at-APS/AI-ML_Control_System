def clean_up():
    for pattern in ["angle.*", "effic.*", "mirr.*", "optax.*", "rmir.*", "screen.*", "star.*"]:
        files = glob.glob(os.path.join(os.curdir, pattern), recursive=True)
        for file in files: os.remove(file)


